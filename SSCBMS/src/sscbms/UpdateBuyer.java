    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String originalMobileNo = jTextField1.getText();
        String name = jTextField2.getText();
        String newMobileNo = jTextField3.getText();
        String email = jTextField6.getText();
        String address = jTextField4.getText();
        String gender = jTextField5.getText();

        try {
            Connection con = ConnectionProvider.getCon();

            PreparedStatement pst = con.prepareStatement("UPDATE buyer SET name=?,mobile=?,email=?,address=?,gender=? WHERE mobile=?;");

            pst.setString(1, name);
            pst.setString(2, newMobileNo);
            pst.setString(3, email);
            pst.setString(4, address);
            pst.setString(5, gender);

            pst.setString(6, originalMobileNo);

            pst.executeUpdate();

            pst.close();
            con.close();

            JOptionPane.showMessageDialog(null, "Successfully Updated!");
            setVisible(false);
            new UpdateBuyer().setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

