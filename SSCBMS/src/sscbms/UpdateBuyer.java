    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String mobileNo = jTextField1.getText();
        try {
             Connection con = ConnectionProvider.getCon();

                PreparedStatement pst = con.prepareStatement("SELECT * FROM buyer WHERE mobile=?;");
                pst.setString(1, mobileNo);

                ResultSet rs = pst.executeQuery();

                if (rs.next()) 
                {
                    jTextField2.setText(rs.getString(1));//name
                    jTextField3.setText(rs.getString(2));//mobile
                    jTextField6.setText(rs.getString(3));//email
                    jTextField4.setText(rs.getString(4));//address
                    jTextField5.setText(rs.getString(5));//gender

                    jTextField1.setEditable(false);
                } 
                else 
                {
                    JOptionPane.showMessageDialog(null, "Mobile Number does not exist!");
                }
                
                rs.close();
                con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

